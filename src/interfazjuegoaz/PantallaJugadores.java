    private void setLista() {
        DefaultListModel<String> jugadores = new DefaultListModel<>();
        for (String jugador : this.juego.listaJugadores) {
            jugadores.addElement(jugador);
        }
        this.jListJugadores.setModel(jugadores);
    }
    
    private void removeJugador(){
        String remueveme = this.jListJugadores.getSelectedValue();
        this.juego.listaJugadores.remove(remueveme);
        setLista();
    }
    
    private void limpiarFormulario() {
        jTxtNombre.setText("");
    }

    private boolean validarCampos() {
        String nombre = jTxtNombre.getText();
        if (nombre.length() < 3) {
            jLblExito.setText("Nombre mÃ¡s de 3 caracteres.");
            jLblExito.setForeground(Color.RED);
            ocultarMensaje();
            return false;
        }
        return true;
    }

    private String generarNombreUnico(String nombre) {
        String nombreUnico = nombre;
        int contador = 1;
        while (this.juego.listaJugadores.contains(nombreUnico)) {
            nombreUnico = nombre + "_" + contador;
            contador++;
        }
        return nombreUnico;
    }

    private void ocultarMensaje() {
        Timer timer = new Timer(3000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                jLblExito.setText("");
            }
        });
        timer.setRepeats(false); // Para que el Timer solo se ejecute una vez
        timer.start();
    }

