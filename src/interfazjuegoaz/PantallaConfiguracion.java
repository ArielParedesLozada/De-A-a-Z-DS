    private void limpiarCampos() {
        jSpTiempo.setValue(0);
        jBtnGrpBotones.clearSelection();
    }

    private String validateFields() {
        StringBuilder error = new StringBuilder();

        // Validar tiempo
        int tiempo = (int) this.jSpTiempo.getValue();
        if (tiempo < 5 || tiempo > 25) {
            error.append("Tiempo entre 5 y 25 segundos.\n");
        }

        // Validar modo
        if (!this.jRbAleatorio.isSelected() && !this.jRbDefault.isSelected()) {
            error.append("Debe seleccionar un modo.\n");
        }

        return error.toString().trim();
    }

    private void ocultarMensaje() {
        Timer timer = new Timer(3000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                jLblMensaje.setText("");
            }
        });
        timer.setRepeats(false); // Para que el Timer solo se ejecute una vez
        timer.start();
    }

