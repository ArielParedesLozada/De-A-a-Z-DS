    private void setDuracion() {
        int resultado;
        try {
            resultado = (int) this.jSpTiempo.getValue();
            if (resultado < 5 || resultado > 25) {
                resultado = Math.min(Math.max(5, resultado), 25); // Asegurar valor entre 5 y 25
            }
        } catch (Exception e) {
            resultado = this.juego.DURACION_DEFAULT;
        }
        this.juego.duracion = resultado;
    }

    private void setModo() {
        if (this.jRbAleatorio.isSelected()) {
            this.juego.listaJugadores.shuffle();
        }
    }

